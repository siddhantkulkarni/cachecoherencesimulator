class ThreadRunner extends Thread {

    String scenario;

    public ThreadRunner(String temp) {
        this.scenario = temp;
    }

    public void run() {
        try {
            if (scenario.trim().length() != 0) {
                Exec.executeScenario(scenario);
            }
        } catch (InterruptedException ex) {
            ex.printStackTrace();
        }

    }
}

